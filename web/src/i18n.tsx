export const I18nContext = React.createContext<{
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: Translate;
} | null>(null);

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('i18n context is missing');
  }
  return ctx;
}

export function initialLang(): Lang {
  const saved = window.localStorage.getItem('nmem-snap-lang');
  if (saved === 'en' || saved === 'zh') return saved;
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

export function makeTranslator(lang: Lang): Translate {
  const text = dictionary[lang];
  return (key: string) => text[key] ?? dictionary.en[key] ?? key;
}

export function persistLang(lang: Lang) {
  window.localStorage.setItem('nmem-snap-lang', lang);
}

export function localeForLang(lang: Lang) {
  return lang === 'zh' ? 'zh-CN' : 'en-US';
}
